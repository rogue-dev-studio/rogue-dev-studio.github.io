#!/usr/bin/env node
/**
 * Strip ALL Co-authored-by / AI attribution trailers from commit messages.
 * Publisher identity = GitHub repo owner only.
 */
import { createInterface } from 'node:readline';

const drop = (line) => {
  const t = line.trim();
  if (/^Co-authored-by:/i.test(t)) return true;
  if (/^Made-with:/i.test(t)) return true;
  if (/^Made with Cursor/i.test(t)) return true;
  if (/^Generated-by:/i.test(t)) return true;
  if (/^Generated by /i.test(t)) return true;
  if (/cursoragent@/i.test(t)) return true;
  return false;
};

const lines = [];
for await (const line of createInterface({ input: process.stdin })) {
  lines.push(line);
}

const out = lines
  .filter((line) => !drop(line))
  .join('\n')
  .replace(/\n{3,}/g, '\n\n')
  .replace(/\s+$/, '\n');

process.stdout.write(out.length ? out : '\n');
